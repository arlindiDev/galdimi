var numbers = [5, -9, 1, 24, -2, 54, -12, 8, -10, -72, -100];

for(var i = 0; i < numbers.length; i++) {
    var positiveNums = negativeNums = 0;
    

    if( numbers[i] > 0) {

        positiveNums += positiveNums;
        

    } else  {

       negativeNums += negativeNums;
       
    } 
    console.log(positiveNums);
    console.log(negativeNums);
    
}